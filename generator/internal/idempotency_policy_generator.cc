IdempotencyPolicyGenerator::IdempotencyPolicyGenerator(
    google::protobuf::ServiceDescriptor const* service_descriptor,
    VarsDictionary service_vars,
    std::map<std::string, VarsDictionary> service_method_vars,
    google::protobuf::compiler::GeneratorContext* context)
    : ServiceCodeGenerator("idempotency_policy_header_path",
                           "idempotency_policy_cc_path", service_descriptor,
                           std::move(service_vars),
                           std::move(service_method_vars), context) {}

Status IdempotencyPolicyGenerator::GenerateHeader() {
  header_.Print(CopyrightLicenseFileHeader());
  header_.Print(service_vars_,  // clang-format off
    "// Generated by the Codegen C++ plugin.\n"
    "// If you make any local changes, they will be lost.\n"
    "// source: $proto_file_name$\n"
    "#ifndef $header_include_guard$\n"
    "#define $header_include_guard$\n"
    "\n");
  // clang-format on

  // includes
  GenerateLocalIncludes(
      header_, {"google/cloud/future.h", "google/cloud/internal/retry_policy.h",
                "google/cloud/status_or.h", "google/cloud/version.h"});
  GenerateSystemIncludes(header_,
                         {service_vars_["proto_grpc_header_path"],
                          "google/longrunning/operations.grpc.pb.h", "memory"});
  header_.Print("\n");

  auto result = OpenNamespaces(header_);
  if (!result.ok()) return result;

  // Abstract interface ConnectionIdempotencyPolicy base class
  header_.Print(service_vars_,  // clang-format off
    "class $idempotency_class_name$ {\n"
    " public:\n"
    "  virtual ~$idempotency_class_name$() = 0;\n"
    "\n");
  // clang-format on

  header_.Print(service_vars_,  // clang-format off
    "  /// Create a new copy of this object.\n"
    "  virtual std::unique_ptr<$idempotency_class_name$> clone() const = 0;\n\n");
  // clang-format on

  for (int i = 0; i < service_descriptor_->method_count(); ++i) {
    auto const& method = *service_descriptor_->method(i);
    PrintMethod(
        method, header_, MergeServiceAndMethodVars(method),
        {MethodPattern(
             {
                 // clang-format off
   {"  virtual google::cloud::internal::Idempotency\n"
    "  $method_name$($request_type$ const& request) = 0;\n"
        "\n",}
                 // clang-format on
             },
             All(IsNonStreaming, Not(IsPaginated))),
         MethodPattern(
             {
                 // clang-format off
   {"  virtual google::cloud::internal::Idempotency\n"
    "  $method_name$($request_type$ request) = 0;\n"
        "\n",}
                 // clang-format on
             },
             All(IsNonStreaming, IsPaginated))},
        __FILE__, __LINE__);
  }

  // close abstract interface Connection base class
  header_.Print(service_vars_,  // clang-format off
    "};\n\n");
  // clang-format on

  header_.Print(service_vars_,  // clang-format off
      "std::unique_ptr<$idempotency_class_name$>\n"
      "    MakeDefault$idempotency_class_name$();\n\n");
  // clang-format on

  CloseNamespaces(header_);
  // close header guard
  header_.Print(service_vars_,  // clang-format off
      "#endif  // $header_include_guard$\n");
  // clang-format on
  return {};
}

Status IdempotencyPolicyGenerator::GenerateCc() {
  cc_.Print(CopyrightLicenseFileHeader());
  cc_.Print(service_vars_,  // clang-format off
    "// Generated by the Codegen C++ plugin.\n"
    "// If you make any local changes, they will be lost.\n"
    "// source: $proto_file_name$\n\n");
  // clang-format on

  // includes
  GenerateLocalIncludes(cc_, {service_vars_["idempotency_policy_header_path"]},
                        FileType::kCcFile);
  GenerateSystemIncludes(cc_, {"memory"});
  cc_.Print("\n");

  auto result = OpenNamespaces(cc_);
  if (!result.ok()) return result;

  cc_.Print("using google::cloud::internal::Idempotency;\n\n");

  cc_.Print(
      service_vars_,  // clang-format off
    "$idempotency_class_name$::~$idempotency_class_name$() = default;\n\n");
  // clang-format on

  // open anonymous namespace
  cc_.Print("namespace {\n");

  cc_.Print(service_vars_,  // clang_format off
            "class Default$idempotency_class_name$ : public "
            "$idempotency_class_name$ {\n"
            " public:\n"
            "  ~Default$idempotency_class_name$() override = default;\n\n"
            //  clang-format on
  );

  cc_.Print(
      service_vars_,  // clang-format off
    "  /// Create a new copy of this object.\n"
    "  std::unique_ptr<$idempotency_class_name$> clone() const override {\n"
    "    return absl::make_unique<Default$idempotency_class_name$>(*this);\n"
    "  }\n\n");
  // clang-format on

  for (int i = 0; i < service_descriptor_->method_count(); ++i) {
    auto const& method = *service_descriptor_->method(i);
    PrintMethod(
        method, cc_, MergeServiceAndMethodVars(method),
        {MethodPattern(
             {
                 // clang-format off
   {"  Idempotency\n"
    "  $method_name$($request_type$ const&) override {\n"
    "    return Idempotency::$default_idempotency$;\n"
    "  }\n\n",}
                 // clang-format on
             },
             All(IsNonStreaming, Not(IsPaginated))),
         MethodPattern(
             {
                 // clang-format off
   {"  Idempotency\n"
    "  $method_name$($request_type$) override {\n"
    "    return Idempotency::$default_idempotency$;\n"
    "  }\n\n",}
                 // clang-format on
             },
             All(IsNonStreaming, IsPaginated))},
        __FILE__, __LINE__);
  }
  cc_.Print(  // clang-format off
    "};\n"
    "}  // namespace\n\n");
  // clang-format on

  cc_.Print(service_vars_,  // clang-format off
      "std::unique_ptr<$idempotency_class_name$>\n"
      "    MakeDefault$idempotency_class_name$() {\n"
      "  return absl::make_unique<Default$idempotency_class_name$>();\n"
      "}\n\n");
  // clang-format on

  CloseNamespaces(cc_);
  return {};
}

